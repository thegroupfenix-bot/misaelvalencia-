/** Get a translation string for a given language code */
export function t(key, lang = "es") {
  return T[lang]?.[key] || T["es"]?.[key] || key;
}

/** Get label from a multilingual label object */
export function tLabel(labelObj, lang = "es") {
  if (!labelObj) return "";
  if (typeof labelObj === "string") return labelObj;
  return labelObj[lang] || labelObj["es"] || labelObj["en"] || Object.values(labelObj)[0] || "";
}

